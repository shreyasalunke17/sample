package IncrementDecrement;
public class Prog8
{
    public static void main(String[] args)
    {
        int a=20;
        a= --a - 1;
        //19 -1 =18
        System.out.println(a);

        int b=20;
        b= b-- - 1;
        //20 - 1=19
        System.out.println(b);
    }
}
























