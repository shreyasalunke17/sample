package IncrementDecrement;
public class PreDecrementBASIC
{
    public static void main(String[] args) {
        int x = 10;
        int y = --x;
        System.out.println("Value of y is: "+y);
    }
}


/* Decrement Operator(--):
The Decrement operator is an operator which is used to decrease the value of the variable by 1, on which it is applied.

Like increment operators,decrement operators are also 2 types:
Pre decrement (--x)
Post decrement (x--)

Pre Decrement Operator:
If a decrement operator is used in front of an operand, then it is called Pre decrement operator.

Syntax: --operand */


/**Pre decrement operator is applied on ‘x’, first,the value of ‘x’ will be decremented by 1 and then the decremented value
 will be assigned to the variable ‘y’.
 As per example,the initial value of ‘x’ is 10. After applying pre decrement operator on ‘x’, the value of ‘x’ is decremented
 by 1 (i.e.9) and that value is assigned to the variable ‘y’. So,when we display the variable ‘y’ it is showing as 9.*/









