package Loops;
public class WhileLoop2
{
    public static void main(String[] args)
    {
        int i = 1;    //initialization expression

        while (i < 6)   //test expression
            {
              System.out.println("Hello World");
              i++;      //update expression

            }
    }
}


/*
Complexity of the above method:
Time Complexity: O(1)
Auxiliary Space : O(1)
*/

























