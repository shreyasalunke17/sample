package Loops;
public class InfiniteWhileLoop1
{
    public static void main(String[] args)
    {
        int i=10;
        while(i>1)
        {
            System.out.println(i);
            i++;
        }
    }
}

/*
The condition is i>1, which never returns false as the initial value of i is 10 and
at every iteration of loop, the value of i is increased using i++.
*/


























