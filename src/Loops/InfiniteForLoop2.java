package Loops;
public class InfiniteForLoop2
{
    public static void main(String[] args)
    {
        for (;;)
        {
            System.out.println("infinitive loop");
        }
    }
}


/*
There is another method for calling the Infinite loop

If we use two semicolons ;; in the for loop, it will be infinitive for a loop.

Syntax:
for(;;)
{
    //code to be executed
}
*/



























