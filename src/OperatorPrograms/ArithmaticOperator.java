package OperatorPrograms;
public class ArithmaticOperator
{
    public static void main(String[] args)
    {
        int a=20;
        int b=40;
        System.out.println("a + b = "+(a+b));
        System.out.println("a - b = "+(a-b));
        System.out.println("a * b = "+(a*b));
        System.out.println("a / b = "+(a/b));
        System.out.println("a % b = "+(a%b));

    }
}

















