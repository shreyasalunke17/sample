package OperatorPrograms;
public class RelationalOperator2
{
    public static void main(String[] args)
    {
        int a=8,b=22;
        System.out.println("a is "+ a +" and b is "+ b);
        System.out.println(a == b);  //false
        System.out.println(a != b);  //true
        System.out.println(a > b);  //false
        System.out.println(a < b);  //true
        System.out.println(a >= b);  //false
        System.out.println(a <= b);  //true
    }
}
